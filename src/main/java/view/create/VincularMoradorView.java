    private void btnVincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVincularActionPerformed
        try {
            ComboItem itemResidencia = (ComboItem) cbResidencia.getSelectedItem();
            if (itemResidencia == null) {
                JOptionPane.showMessageDialog(this, "Selecione uma residência.");
                return;
            }

            int idResidencia = itemResidencia.getId();
            boolean responsavel = cbResponsavel.isSelected();

            moradorController.vincularMoradorResidencia(idMorador, idResidencia, responsavel);

            JOptionPane.showMessageDialog(this, "Morador vinculado com sucesso!");

            dispose(); // fecha a janela

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao vincular: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnVincularActionPerformed

