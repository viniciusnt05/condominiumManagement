    private void carregarResidencias() {
        try {
            cbResidencia.removeAllItems();

            List<Map<String, Object>> listaResidencias = residenciaController.listarResidencias();

            listaResidencias.forEach(item -> {
                int id = (int) item.get("id");
                String descricao = (String) item.get("endereco");
                cbResidencia.addItem(new ComboItem(id, descricao));
            });
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar residências: " + e.getMessage());
            e.printStackTrace();
        }
    }

