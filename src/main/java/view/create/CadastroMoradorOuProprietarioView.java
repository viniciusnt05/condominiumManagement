    public void carregarComboPessoa() throws SQLException {
        List<Pessoa> pessoas = pessoaController.listarTodos();
        cmbPessoa.removeAllItems();

        pessoas.forEach(p -> 
            cmbPessoa.addItem(new ComboItem(p.getId(), p.getNome() + " - " + p.getCpf()))
        );
    }

    public void selecionarPessoaNoCombo(ComboItem item) {
        IntStream.range(0, cmbPessoa.getItemCount())
            .filter(i -> ((ComboItem) cmbPessoa.getItemAt(i)).getId() == item.getId())
            .findFirst()
            .ifPresent(cmbPessoa::setSelectedIndex);
    }

