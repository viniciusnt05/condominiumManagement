    private void btnCadastrarProprietarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProprietarioActionPerformed
        try {
            ComboItem itemSelecionado = (ComboItem) cmbPessoa.getSelectedItem();
            if (itemSelecionado == null) {
                JOptionPane.showMessageDialog(this, "Selecione uma pessoa.", "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int idPessoa = itemSelecionado.getId();

            // Verifica se já é proprietário
            if (proprietarioController.pessoaJaEhProprietario(idPessoa)) {
                JOptionPane.showMessageDialog(this, "Esta pessoa já é proprietária.", "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Pessoa pessoa = pessoaController.buscarPorId(idPessoa);

            proprietarioController.cadastrarProprietario(pessoa);

            JOptionPane.showMessageDialog(this, "Proprietário cadastrado com sucesso!");
            // limparCampos(); // se quiser limpar campos depois

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar proprietário: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnCadastrarProprietarioActionPerformed

