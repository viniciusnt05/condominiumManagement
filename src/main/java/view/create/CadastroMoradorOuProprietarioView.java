    private void btnCadastrarMoradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarMoradorActionPerformed
        try {
            ComboItem itemSelecionado = (ComboItem) cmbPessoa.getSelectedItem();
            if (itemSelecionado == null) {
                JOptionPane.showMessageDialog(this, "Selecione uma pessoa.", "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int idPessoa = itemSelecionado.getId();

            // Verificar se já é morador
            if (moradorController.pessoaJaEhMorador(idPessoa)) {
                JOptionPane.showMessageDialog(this, "Esta pessoa já é um morador!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return; // Para não continuar o cadastro
            }

            Pessoa pessoa = pessoaController.buscarPorId(idPessoa);
            int idMorador = moradorController.cadastrarMorador(pessoa);

            JOptionPane.showMessageDialog(this, "Morador cadastrado com sucesso!");

            Object[] opcoes = {"Sim", "Não"};
            int opcao = JOptionPane.showOptionDialog(this,
                "Deseja vincular este morador a uma residência?",
                "Vincular Residência",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opcoes,
                opcoes[0]);

            if (opcao == JOptionPane.YES_OPTION) {
                new VincularMoradorView(null, idMorador).setVisible(true);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar morador: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnCadastrarMoradorActionPerformed

