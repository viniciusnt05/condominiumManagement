    public void carregarDados() {
        try {
            List<Map<String, Object>> lista = controller.listarResidenciasComDetalhes();

            String[] colunas = {
                "ID", "Rua", "Número", "CEP", "Proprietário",
                "Qtd Moradores", "Responsável", "Em Dia"
            };
            DefaultTableModel modelo = new DefaultTableModel(colunas, 0);

            lista.forEach(item -> {
                try {
                    int idResidencia = (int) item.get("id");
                    boolean emDiaReal = controller.verificarPagamentosEmDia(idResidencia);

                    modelo.addRow(new Object[]{
                        item.get("id"),
                        item.get("rua"),
                        item.get("numero"),
                        item.get("cep"),
                        item.get("proprietario"),
                        item.get("qtde_moradores"),
                        item.get("responsavel"),
                        emDiaReal ? "Sim" : "Não"
                    });
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Erro ao verificar pagamentos da residência ID " + item.get("id") + ": " + e.getMessage());
                    e.printStackTrace();
                }
            });

            tblResidencia.setModel(modelo);
            // Oculta a coluna ID
            tblResidencia.getColumnModel().getColumn(0).setMinWidth(0);
            tblResidencia.getColumnModel().getColumn(0).setMaxWidth(0);
            tblResidencia.getColumnModel().getColumn(0).setWidth(0);
            tblResidencia.getColumnModel().getColumn(0).setPreferredWidth(0);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados: " + ex.getMessage());
            ex.printStackTrace();
        }
    }

