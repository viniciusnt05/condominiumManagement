    private void btnPagamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagamentosActionPerformed
        int linhaSelecionada = tblResidencia.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma residência primeiro!");
            return;
        }

        int idResidencia = (int) tblResidencia.getModel().getValueAt(linhaSelecionada, 0);

        PagamentoView pagamentoView = new PagamentoView(idResidencia, this);
        pagamentoView.setVisible(true);
    }//GEN-LAST:event_btnPagamentosActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linhaSelecionada = tblResidencia.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma residência para editar.");
            return;
        }

        int idResidencia = (int) tblResidencia.getModel().getValueAt(linhaSelecionada, 0);

        EditarResidenciaView editarView = new EditarResidenciaView(this, idResidencia);
        editarView.carregarProprietarios(); // <-- Carrega primeiro o ComboBox
        editarView.carregarDadosResidencia(idResidencia); // <-- Depois preenche os dados e seleciona o ComboBox
        editarView.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

