    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblResidencia.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma residência para deletar.");
            return;
        }

        int opcao = JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja excluir esta residência?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {
            try {
                int idResidencia = (int) tblResidencia.getValueAt(linhaSelecionada, 0);

                controller.deletarResidencia(idResidencia);

                JOptionPane.showMessageDialog(this, "Residência deletada com sucesso.");

                carregarDados();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao deletar residência: " + ex.getMessage());
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

