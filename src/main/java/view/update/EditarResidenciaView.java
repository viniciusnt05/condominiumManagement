    public void carregarProprietarios() {
        try {
            cmbProprietario.removeAllItems();

            cmbProprietario.addItem(new ComboItem(-1, "Sem proprietário"));

            List<Map<String, Object>> proprietarios = proprietarioController.listarProprietariosComDadosDistinct();

            proprietarios.forEach(p -> {
                int id = (int) p.get("id");
                String nome = (String) p.get("nome");
                String cpf = (String) p.get("cpf");
                String descricao = nome + " - " + cpf;

                cmbProprietario.addItem(new ComboItem(id, descricao));
            });

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar proprietários: " + e.getMessage());
            e.printStackTrace();
        }
    }

    public void carregarDadosResidencia(int idResidencia) {
        try {
            this.idResidencia = idResidencia;
            Map<String, Object> dados = residenciaController.buscarResidenciaPorId(idResidencia);

            if (dados != null) {
                txtRua.setText((String) dados.get("rua"));
                txtNumero.setText(String.valueOf(dados.get("numero")));
                txtCEP.setText((String) dados.get("cep"));

                int idProprietario = (int) dados.get("id_proprietario");

                // Seleciona o proprietário certo (versão lambda)
                IntStream.range(0, cmbProprietario.getItemCount())
                        .filter(i -> ((ComboItem) cmbProprietario.getItemAt(i)).getId() == idProprietario)
                        .findFirst()
                        .ifPresent(i -> cmbProprietario.setSelectedIndex(i));

                cbEmDia.setSelected((boolean) dados.get("em_dia"));

            } else {
                JOptionPane.showMessageDialog(this, "Residência não encontrada.");
                dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar residência: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
