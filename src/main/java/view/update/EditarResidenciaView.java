    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            String rua = txtRua.getText().trim();
            String numeroStr = txtNumero.getText().trim();
            String cep = txtCEP.getText().trim();
            ComboItem proprietarioSelecionado = (ComboItem) cmbProprietario.getSelectedItem();
            boolean emDia = cbEmDia.isSelected();

            if (rua.isEmpty() || numeroStr.isEmpty() || cep.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
                return;
            }

            int numero = Integer.parseInt(numeroStr);

            Integer idProprietario = (proprietarioSelecionado != null && proprietarioSelecionado.getId() != -1)
                    ? proprietarioSelecionado.getId()
                    : null;

            residenciaController.editarResidencia(idResidencia, rua, numero, cep, idProprietario, emDia);

            JOptionPane.showMessageDialog(this, "Residência atualizada com sucesso!");

            if (residenciaView != null) {
                residenciaView.carregarDados();
            }

            dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Número inválido.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar residência: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

